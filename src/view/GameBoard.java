    public String getSelectedAvion() {
        String avion = "";
        //selection de l avion
        if (jRadioButton5.isSelected()) {
            avion = "MiG-51S";
            System.out.println("Avion " + avion);

        } else if (jRadioButton6.isSelected()) {
            avion = "F_A-28A-mini";
            System.out.println("Avion " + avion);

        } else if (jRadioButton7.isSelected()) {
            avion = "Su-55";
            System.out.println("Avion " + avion);

        } else if (jRadioButton8.isSelected()) {
            avion = "Su-51K-mini";
            System.out.println("Avion " + avion);

        }
        return avion;
    }

    public Integer getSelectedLvl() {
        Integer lvl = 0;
//choix du niveau
        if (jRadioButton1.isSelected()) {
            lvl = 1;
            System.out.println("Niveau " + lvl);
        } else if (jRadioButton2.isSelected()) {
            lvl = 2;
            System.out.println("Niveau " + lvl);
        } else if (jRadioButton3.isSelected()) {
            lvl = 3;
            System.out.println("Niveau " + lvl);
        } else if (jRadioButton4.isSelected()) {
            lvl = 4;
            System.out.println("Niveau " + lvl);
        }
        return lvl;
    }

    public Partie getParams() {
        String avion = getSelectedAvion();
        Integer lvl = getSelectedLvl();
        String joueur = jTextField1.getText().toString();
        boolean audio = jToggleButton1.isSelected();
        System.out.println("etat " + audio);
        Partie p = new Partie(joueur, avion, lvl, audio);
        return p;
    }

